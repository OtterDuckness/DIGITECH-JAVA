
package com.mycompany.practicapoometodos12;


public class Areas {
    
    public double circulo (double r) //calculamos area de un circulo
    {
        double circ = 3.14 * r * r;
        return circ;
    }
    
    public int rectangulo (int a, int b) //calculamos area de un rectangulo
    {
        int rect = a * b;
        return rect;
    }
}
